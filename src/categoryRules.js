// 카테고리(주제) 이름에서 '숨은 의도(패턴)'를 코드가 먼저 분류해 도메인별 출제 규칙을 더한다.
// 반환: { mask, subRule }
//  - mask: { revealCount } 가 있으면 코드가 정답 앞 N글자만 남기고 가린다(빈칸 채우기 유형).
//  - subRule: 사용자 프롬프트에 덧붙일 도메인별 출제 지시 한두 줄.
// 사용자가 형식 예시(typeHint)를 직접 준 경우엔 그 예시가 우선이므로 이 분류는 적용하지 않는다(호출부에서 처리).
export function classifyCategory(name) {
  const n = String(name || '');

  // 1) 사자성어 → 4글자 중 앞 2글자만 보이고 나머지는 코드가 가림(글자 완성 퀴즈)
  if (n.includes('사자성어')) {
    return {
      mask: { revealCount: 2 },
      subRule: '- 정답은 널리 알려진 4글자 사자성어 전체로 하세요(예: 오비이락).',
    };
  }

  // 2) 속담 → 실존 속담의 뜻/상황 설명, 정답은 속담 전체
  if (n.includes('속담')) {
    return {
      mask: null,
      subRule:
        '- 실제로 널리 쓰이는 속담만 사용하고, 속담의 뜻이나 쓰임 상황을 설명한 뒤 정답이 그 속담 전체가 되도록 출제하세요.',
    };
  }

  // 3) 대중문화·미디어 → 실존 정보만(환각 방지)
  if (/가요|노래|대중음악|음악|k-?pop|팝송|힙합|영화|드라마|예능|애니|만화|아이돌|배우|가수|연예/i.test(n)) {
    return {
      mask: null,
      subRule:
        '- 실존하고 대중적으로 유명한 정보(가수·곡명·작품·인물·대사)만 사용하고, 가상의 제목·인물·가사를 절대 지어내지 마세요.',
    };
  }

  // 4) 인물·역사 → 업적/사건 설명 → 이름/사건명 정답
  if (/인물|위인|역사|왕조|전쟁|사건/.test(n)) {
    return {
      mask: null,
      subRule:
        '- 특정 인물의 업적이나 사건의 내용을 설명하고, 정답이 그 인물의 이름 또는 사건명이 되도록 출제하세요.',
    };
  }

  // 5) 그 외 → 시스템 프롬프트의 사실 기반 규칙을 그대로 따른다(추가 규칙 없음)
  return { mask: null, subRule: '' };
}
