// 출제 형식 지시. 프리픽스 마스킹(maskTemplate)이면 가림은 앱이 처리하므로 AI는 정답만 만든다.
function buildTypeHintInstruction(typeHint, maskTemplate) {
  if (maskTemplate) {
    const n = maskTemplate.revealCount;
    return (
      `\n[출제 형식 — 매우 중요]\n` +
      `이 카테고리는 '정답의 앞 ${n}글자만 보여주고 나머지는 가리는' 형식입니다. ` +
      `가리는 처리는 앱이 자동으로 하므로 당신은 정답만 만들면 됩니다.\n` +
      `- answer: 가리지 않은 '완전한 정답 전체'를 넣으세요. (예: 인터스텔라)\n` +
      `- question: answer와 동일하게 완전한 정답 전체를 넣으세요(앱이 앞 ${n}글자만 남기고 가립니다). 절대 직접 가리거나 괄호를 넣지 마세요.\n` +
      `- 정답은 이 카테고리 주제에 실제로 존재하는 항목이어야 하며, 반드시 ${n + 1}글자 이상이어야 합니다.\n` +
      `- hint는 정답을 직접 쓰지 말고 짧은 단서로 작성하세요.`
    );
  }
  if (!typeHint) return '';
  return (
    `\n[출제 형식 — 매우 중요, 시스템의 '정답 노출 금지' 규칙보다 우선]\n` +
    `이 카테고리는 특수 출제 형식입니다. 아래 예시와 똑같은 형식으로 question을 만드세요:\n` +
    `예시: ${typeHint}\n` +
    `- question 필드에는 예시처럼 일부가 가려지거나 변형된 형태를 넣으세요. ` +
    `정답의 일부가 question에 보여도 됩니다(형식상 의도된 것).\n` +
    `- answer 필드에는 가려지지 않은 완전한 정답을 넣으세요.\n` +
    `- hint는 정답을 직접 쓰지 말고 짧은 단서로 작성하세요.`
  );
}

export function buildQuestionUserPrompt({
  category,
  difficulty,
  count,
  excludeKeywords = [],
  wantTheme = false,
  diversity = null,
  typeHint = '',
  maskTemplate = null,
  subRule = '',
}) {
  // A-1 누적 제외 목록 — 정답·소재 모두 반복 금지
  const exclude =
    excludeKeywords.length > 0
      ? `\n다음 정답들은 이미 출제되었으니 절대 반복하지 마라:\n${excludeKeywords.join(', ')}\n` +
        '위 목록과 정답이 같거나, 핵심 소재·주제가 겹치는 문제도 출제하지 마라.'
      : '';

  // A-2 다양성 축 — 시드 + 세부주제 + 관점 로테이션
  let axes = '';
  if (diversity) {
    const parts = [`\n이번 배치 다양성 시드: ${diversity.seed}. 이 시드에 맞춰 평소와 다른 소재를 선택하라.`];
    if (diversity.subtopics?.length) {
      parts.push(`이번 배치는 다음 세부 주제 위주로 출제하라: ${diversity.subtopics.join(', ')}.`);
    }
    if (diversity.perspective) {
      parts.push(`이번 배치는 '${diversity.perspective}' 관점으로 출제하라.`);
    }
    axes = parts.join('\n');
  }

  return (
    `카테고리: ${category}. 난이도: '${difficulty}'. ${count}문제를 출제하세요. ` +
    `${count}문제 전부가 '${difficulty}' 난이도(시스템 규칙의 예상 정답률 범위)여야 합니다.` +
    exclude +
    axes +
    (subRule ? `\n[이 카테고리 출제 규칙]\n${subRule}` : '') +
    buildTypeHintInstruction(typeHint, maskTemplate) +
    (wantTheme ? THEME_INSTRUCTION : '')
  );
}

